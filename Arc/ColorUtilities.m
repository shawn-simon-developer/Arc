#import "ColorUtilities.h"

@implementation ColorUtilities

+ (UIColor *) flatBlackColor
{
    return [UIColor blackColor];
}

+ (UIColor *) flatDarkGrayColor
{
    return [UIColor colorWithRed:53/255.0 green:53/255.0 blue:55/255.0 alpha:1.0];
}

@end
